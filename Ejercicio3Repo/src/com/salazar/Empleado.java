package com.salazar;

public class Empleado {

    public String nombre;
    public String correo;
    public double salario;
    public String puesto;

    public Empleado(String nombre, String correo, Double salario, String puesto) {
        this.nombre = nombre;
        this.correo = correo;
        this.salario = salario;
        this.puesto = puesto;
    }
}
